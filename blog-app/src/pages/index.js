import Head from 'next/head'
import Link from 'next/link';
import { getAllPublished } from './notion';
import styles from '../styles/Home.module.css'

export default function Home({ posts }) {
  // console.log(posts)
  if (posts.length === 0) return <>
    <main>
      <h1 className='text-center my-3'>Blogs</h1>
      <section className='p-3 my-4 shadow-sm p-3 mb-5 bg-body-tertiaryborder rounded'>
        <p className='text-center my-3'>No posts</p>
      </section>

    </main>
  </>

  return (
    <div className={styles.container}>
      <Head>
        <title>My Blog</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <nav>
        <div className='d-flex justify-content-between my-3 fs-3'>
          <div>
            <Link href={'/'}> </Link>
          </div>
          <div>
            <Link href={'/'} className='text-decoration-none text-reset'> Blogs</Link>
          </div>
          <div>
            <button className='btn-sm'><Link href={'/'} className='text-reset text-decoration-none p-2'> Home</Link></button>
          </div>
        </div>
      </nav>
      <main className={styles.main}>

        <div className="container">
          {posts.map((post, index) => (
            <section key={index} className='p-3 my-4 shadow-sm p-3 mb-5 bg-body-tertiaryborder rounded'>
              <div>
                <h6>
                  <Link href={post.slug} className='text-reset'>
                    {post.title}
                  </Link>
                </h6>
              </div>
              <div>{post.date}</div>
              <p>{post.description.substr(0, 100) + " . . ."}</p>
            </section>
          ))}

        </div>
      </main >
    </div >
  )
}

export const getStaticProps = async () => {
  const data = await getAllPublished()
  console.log(data)
  return {
    props: {
      posts: data,
    },
    revalidate: 60
  };
};
